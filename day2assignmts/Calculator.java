package week1.day2assignmts;

public class Calculator {
	
public int doAddition(int num1, int num2, int num3) 
{
	int sum = num1+ num2 + num3;
	
	return sum;
}

public int doSubtraction(int i, int j)
{
	int difference = i - j;
	return difference;
}

public double doMultiplication(double i, double j)
{
	double output = i * j;
	return output;
}

public float doDivision(float i, float j)
{
	float output = i / j;
	return output;
}



}
